        <h3 class="text-danger">Workshop on Modern Application Development</h3><hr>
 I-Tech Club and Department of Computer Science and Engineering had organized 07 days Faculty Development Program and Student workshop on Modern Application Development during March 22-29, 2018. Participants were thoroughly explored through PHP and .NET framework programming. They were taught from the basics to practical implementations of website development using SQL as well. Such a detailed workshop (07 days) has been conducted for the first time in the entire Region and was a positive success among the students and faculties. They were enlightened and found themselves technically rejuvenated  in the end.
 <h3 class="text-danger">Workshop on PYTHON Technology</h3><hr>
 <p>A workshop was organized by Computer Science & Engineering Department for Courses M.Tech, B.Tech. on 16th and 17th March 2018. The resource person for the workshop was Mr. Jeetendra Singh from RCPL, Noida. Workshop was all about Python, Bigdata and Machine Learning Technology. This was a learning and knowledge enhancing session for the students in which they learnt How to work with techniques and enhancing their technical skills.</p>
