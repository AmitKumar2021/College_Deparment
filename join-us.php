    <h2> Join Us </h2>
    <p><p> The <a href="http://www.cse.iitd.ernet.in">Department of Computer Science and Engineering</a> at <a href="http://www.iitd.ernet.in">IIT Delhi</a> is renowned for cutting edge research and for imparting state of the art education. We attract some of the brightest students and faculty. We invite you to join us in the excitement and be an active participant in the growth of the department.</p>
<p> </p>

<div id="admissionTitle">
          <a style="text-decoration:none" href="javascript:toggleMe(1,1)"><span style="font-size:1.4em; padding-right:20px">Faculty</span><img src="image/icon_dropdown_hover.png" alt="click here to expand" width="14px" height="14px"/></a>
            </div><br>
            <div id="description1" style="display:none" >
                <p><span class="normalLink">The <a href="http://www.cse.iitd.ernet.in/">Department of Computer Science and Engineering</a> at <a href="http://www.iitd.ernet.in/">IIT Delhi</a> is actively seeking applications for faculty positions at all levels in all areas of Computer Science and Engineering. Applications for sabbatical and short-term positions are also welcome. For more information please direct all enquiries to <a href="mailto:head%5E@%5Ecse.iitd.ernet.in">head
                        [AT] cse.iitd.ernet.in</a> or see the latest <a href="http://www.iitd.ernet.in/jobs/index.html">faculty advertisement</a>. <br /><br />In addition to its already strong and well known undergraduate programme, CSE at IITD has also emerged as a leading department for research and graduate studies in Computer Science and Engineering in India. Following are some reasons as to why CSE@IITD is an attractive place to join as a faculty member.</span></p>
                <p><a href="/index.php/2011-12-29-23-14-30/join-us?id=47#research">Strong research emphasis and congenial environment</a></p>
                <p><a href="/index.php/2011-12-29-23-14-30/join-us?id=47#graduate">Strong graduate programme</a></p>
                <p><a href="/index.php/2011-12-29-23-14-30/join-us?id=47#benefits">Faculty benefits</a></p>
                <p><a href="/index.php/2011-12-29-23-14-30/join-us?id=47#quality">Quality of campus living</a></p>
                <p> </p>
                <hr />
                <h4><a name="research"></a>Strong research emphasis and congenial environment</h4>
                <ol>
                    <li>The department has a strong <a href="/index.php/2011-12-29-23-14-30/join-us?id=7">faculty</a>, all Ph. D.'s from leading universities and active in research. The department also attracts a regular stream of visiting faculty members. This provides excellent opportunities for collaboration and joining existing research groups.</li>
                    <li>The department has a strong focus on research and provides an excellent research environment. The department already has strong groups in many <a href="/index.php/2011-12-29-23-14-30/join-us?id=15">research areas</a> and is looking to grow in other areas of CSE.</li>
                    <li>The department regularly publishes in leading Journals and Conferences in CSE. Check out the sample <a href="/index.php/2011-12-29-23-14-30/join-us?id=16">list of publications</a>.</li>
                    <li>The department carries out a significant number of <a href="http://www.cse.iitd.ac.in/research/research_projects.shtml">sponsored projects</a> funded by both industry and government agencies. With a large number of multinational and technology giants opening up operations in India, the opportunities for sponsored research and consultancy assignments are excellent. The department provides encouragement and support to new faculty members to build up
                    research activity through these channels.</li>
                    <li>All new faculty members are supported with seed money (at present about Rs. 11,00,000) to kickstart their research activity.</li>
                    <li>New faculty members have the choice of joining existing research groups or of setting up new facilities and areas. In any case all facilities and resources of the department are available to all faculty members without any group boundaries.</li>
                    <li>The department promotes and encourages inter-disciplinary research, both within the department and outside.</li>
                    <li>Finally, the department has excellent infrastructure with laboratories supported by Microsoft, Cisco, Philips, Oracle and RedHat. The department is soon shifting to a well-appointed, centrally air-conditioned new building.</li>
                </ol>
                <h4><a name="graduate"></a>Strong graduate programme</h4>
                <ol>
                    <li>The department has one of the strongest Ph. D. programmes in CSE in the country with a growing number of <a href="/index.php/2011-12-29-23-14-30/join-us?id=8">Ph. D. students</a>. Our graduates have done extremely well. Click <a href="/index.php/2011-12-29-23-14-30/join-us?id=32">here</a> to see where some of our PhD graduates are working. Highlights of some achievements of our Ph. D. students can be seen <a
                        href="/index.php/2011-12-29-23-14-30/join-us?id=18">here</a> and also check out the sample <a href="/index.php/2011-12-29-23-14-30/join-us?id=60">Ph. D. publications</a>.</li>
                    <li>The department runs a 2-year <a href="/index.php/2011-12-29-23-14-30/join-us?id=33">Masters by Research (MSR)</a> programme for students with exceptionally strong background in CSE. With only a semester of course work, the student spends about an year and a half on a thesis under the supervision of a faculty member.</li>
                    <li>The department conducts a five year integrated <a href="/index.php/2011-12-29-23-14-30/join-us?id=42">Dual-degree</a> (B. Tech. and M. Tech.) programme in CSE. The entry into the programme is through the <a href="http://www.iitd.ac.in/jee/">JEE</a> along with the B. Techs. The dual-degree students typically devote close to two years for the masters thesis work.</li>
                    <li>The department runs a two year <a href="/index.php/2011-12-29-23-14-30/join-us?id=37">M. Tech. in CSE</a> in with a full year of thesis work.</li>
                    <li>The department also participates in interdisciplinary Master's programmes viz. <a href="http://www.iitd.ernet.in/vdtt">M. Tech. in VLSI Design Tools and Technology</a> and an M. Tech. in Mathematics and Computer Applications.. Both the programmes have strong research components with a full year of thesis work.</li>
                    <li>Check out the sample <a href="/index.php/2011-12-29-23-14-30/join-us?id=59">Masters' publications</a>.</li>
                    <li>Finally our undergraduates also have a strong tradition of carrying out research in the <a href="/index.php/2011-12-29-23-14-30/join-us?id=44">B. Tech.</a> programme. Check out the sample <a href="/index.php/2011-12-29-23-14-30/join-us?id=58">undergraduate publications</a>.</li>
                </ol>
                <h4><a name="benefits"></a>Faculty benefits</h4>
                <p><span class="normalLink">Faculty members at IITD enjoy several benefits. Some of these are</span></p>
                <ol>
                    <li>On-campus housing with free broadband Internet connections and telephone bill reimbursements.</li>
                    <li>Access to free on-campus medical care with substantial reimbursements for treatment costs in other recognized hospitals.</li>
                    <li>Contingency grants which may be used to pay membership of professional societies or for buying books.</li>
                    <li>Travel support for attending national and international conferences through IITD travel grants, IBM-IRL conference travel support for IITD faculty and support from other government agencies.</li>
                    <li>Flexibility to take up Industrial consultancy assignments.</li>
                    <li>Alumni Pankaj Gupta (BTech 1991) has sponsored a <a href="http://www.cse.iitd.ernet.in/index.php?option=com_content&view=article&id=111">Young Faculty Award</a>.</li>
                </ol>
                <h4><a name="quality"></a>Quality of campus living</h4>
                <ol>
                    <li>IITD has an excellent, green, spacious and peaceful campus right in the heart of south Delhi with all the amenities of the city easily accessible.</li>
                    <li>The campus has excellent facilities which include shopping centres, a well equipped health centre and sports facilities which include tennis, squash, badminton, table tennis, billiards and snooker, gymnasiums, swimming pool, jogging tracks and cricket, football and hockey grounds.</li>
                    <li>The campus has regular cultural events which include music and dance programmes, movies and various other activities organized by students.</li>
                    <li>Delhi arguably has the best civic amenities of all Indian cities. It also has some of the best schools in the country and provides excellent employment opportunities for spouses.</li>
                    <li>Delhi is also perhaps one of the best tourist attractions in the country with a recorded history of a 1000 years. Apart from its rich Mughal and British heritage, Delhi provides easy access to a plethora of tourist spots. Agra, Jaipur, Mathura, Alwar, Bharathpur, Dehradun, Haridwar, Rishikesh, Chandigarh, Kasauli, Shimla, Corbet National Park and Nainital are all within a few hours drive from Delhi. Delhi also provides the best access to Rajasthan, Himachal, Punjab,
                    Jammu and Kashmir, Garhwal, Kumaon and the upper Himalayas.</li>
                </ol>
